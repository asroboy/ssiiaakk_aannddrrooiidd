package com.uika.siak.object;

public class Tugas {

	private String code;
	private String codeMatKul;
	private String namaTugas;
	private String tanggalDeadline;
	private String jenisTugas;
	private String waktuPengingat;
	private String status;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCodeMatKul() {
		return codeMatKul;
	}

	public void setCodeMatKul(String codeMatKul) {
		this.codeMatKul = codeMatKul;
	}

	public String getNamaTugas() {
		return namaTugas;
	}

	public void setNamaTugas(String namaTugas) {
		this.namaTugas = namaTugas;
	}

	public String getTanggalDeadline() {
		return tanggalDeadline;
	}

	public void setTanggalDeadline(String tanggalDeadline) {
		this.tanggalDeadline = tanggalDeadline;
	}

	public String getJenisTugas() {
		return jenisTugas;
	}

	public void setJenisTugas(String jenisTugas) {
		this.jenisTugas = jenisTugas;
	}

	public String getWaktuPengingat() {
		return waktuPengingat;
	}

	public void setWaktuPengingat(String waktuPengingat) {
		this.waktuPengingat = waktuPengingat;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
